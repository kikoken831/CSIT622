public class StatsObject {

	String eventName;
	double mean;
	double SD;
	
	public String getEventName() {
		return eventName;
	}
	
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	
	public double getMean() {
		return mean;
	}
	
	public void setMean(double mean) {
		this.mean = mean;
	}
	
	public double getSD() {
		return SD;
	}
	
	public void setSD(double sD) {
		SD = sD;
	}
	
}
